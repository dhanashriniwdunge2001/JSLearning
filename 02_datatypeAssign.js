
// String
let str = "hello ";
console.log(str + "</br>");

// number
const num = 10;
console.log(num + "</br>");

// boolean
const x = "true";
console.log(x + "</br>");

// undefined
let name;
console.log(name + "</br>");

// null
const number = null;
console.log(number + "</br>");

// symbol
const value1 = Symbol("hello");
const value2 = Symbol("hello");
console.log(value1 + "</br>");
console.log(value2 + "</br>");

